/* Private macro -------------------------------------------------------------*/
#define    BOOT_TIME         10 //ms

/* Private variables ---------------------------------------------------------*/
static uint8_t tx_buffer[1000];
static iis2dulpx_ah_qvar_mode_t qvar_mode;
static stmdev_ctx_t dev_ctx;
static iis2dulpx_status_t status;
static iis2dulpx_priv_t priv_data;

/* Extern variables ----------------------------------------------------------*/

/* Private functions ---------------------------------------------------------*/
/*
 *   WARNING:
 *   Functions declare in this section are defined at the end of this file
 *   and are strictly related to the hardware platform used.
 *
 */
static int32_t platform_write(void *handle, uint8_t reg, const uint8_t *bufp,
                              uint16_t len);
static int32_t platform_read(void *handle, uint8_t reg, uint8_t *bufp,
                             uint16_t len);
static void tx_com( uint8_t *tx_buffer, uint16_t len );
static void platform_delay(uint32_t ms);
static void platform_init(void);

/* Main Example --------------------------------------------------------------*/
static uint8_t qvar_drdy_event = 0;

void iis2dulpx_qvar_drdy_handler(void)
{
  iis2dulpx_status_get(&dev_ctx, &status);
  if (status.drdy) {
    qvar_drdy_event = 1;
  }
}

void iis2dulpx_qvar_read_data(void)
{
  iis2dulpx_pin_int_route_t int_route;
  uint8_t id;
  iis2dulpx_md_t md;
  iis2dulpx_ah_qvar_data_t data;
  iis2dulpx_xl_data_t data_xl;

  /* Initialize mems driver interface */
  dev_ctx.write_reg = platform_write;
  dev_ctx.read_reg = platform_read;
  dev_ctx.mdelay = platform_delay;
  dev_ctx.handle = &SENSOR_BUS;
  dev_ctx.priv_data = &priv_data;

  /* Initialize platform specific hardware */
  platform_init();

  /* Wait sensor boot time */
  platform_delay(BOOT_TIME);

  iis2dulpx_exit_deep_power_down(&dev_ctx);

  /* Check device ID */
  iis2dulpx_device_id_get(&dev_ctx, &id);
  if (id != IIS2DULPX_ID)
    while(1);

  /* Restore default configuration */
  iis2dulpx_sw_reset(&dev_ctx);

  /* init bdu and add_inc */
  iis2dulpx_init_set(&dev_ctx);

  /* Configure interrupt pins */
  int_route.drdy       = PROPERTY_ENABLE;
  int_route.int_on_res = PROPERTY_ENABLE;
  iis2dulpx_pin_int1_route_set(&dev_ctx, &int_route);

  /* Set Output Data Rate */
  md.fs =  IIS2DULPX_4g;
  md.bw = IIS2DULPX_ODR_div_4;
  md.odr = IIS2DULPX_25Hz_LP;
  iis2dulpx_mode_set(&dev_ctx, &md);

  /* Enable AH/QVAR function */
  qvar_mode.ah_qvar_en = 1;
  qvar_mode.ah_qvar_zin = IIS2DULPX_520MOhm;
  qvar_mode.ah_qvar_gain = IIS2DULPX_GAIN_0_5;
  iis2dulpx_ah_qvar_mode_set(&dev_ctx, qvar_mode);

  /* Read wvar samples at drdy */
  while(1)
  {
    /* Read output only if new values are available */
    if (qvar_drdy_event) {
      qvar_drdy_event = 0;

      iis2dulpx_ah_qvar_data_get(&dev_ctx, &data);
      iis2dulpx_xl_data_get(&dev_ctx, &md, &data_xl);

      snprintf((char*)tx_buffer, sizeof(tx_buffer),"Acceleration [mg]:%4.2f\t%4.2f\t%4.2f - QVAR [LSB]: %d\r\n",
               data_xl.mg[0], data_xl.mg[1], data_xl.mg[2], data.raw);
      tx_com(tx_buffer, strlen((char const*)tx_buffer));
    }
  }
}

/*
 * @brief  Write generic device register (platform dependent)
 *
 * @param  handle    customizable argument. In this examples is used in
 *                   order to select the correct sensor bus handler.
 * @param  reg       register to write
 * @param  bufp      pointer to data to write in register reg
 * @param  len       number of consecutive register to write
 *
 */
static int32_t platform_write(void *handle, uint8_t reg, const uint8_t *bufp,
                              uint16_t len)
{
#if defined(NUCLEO_F401RE)
  HAL_I2C_Mem_Write(handle, IIS2DULPX_I2C_ADD_L, reg,
                    I2C_MEMADD_SIZE_8BIT, (uint8_t*) bufp, len, 1000);
#elif defined(STEVAL_MKI109V3)
  HAL_GPIO_WritePin(CS_up_GPIO_Port, CS_up_Pin, GPIO_PIN_RESET);
  HAL_SPI_Transmit(handle, &reg, 1, 1000);
  HAL_SPI_Transmit(handle, (uint8_t*) bufp, len, 1000);
  HAL_GPIO_WritePin(CS_up_GPIO_Port, CS_up_Pin, GPIO_PIN_SET);
#elif defined(SPC584B_DIS)
  i2c_lld_write(handle,  IIS2DULPX_I2C_ADD_L & 0xFE, reg, (uint8_t*) bufp, len);
#endif
  return 0;
}

/*
 * @brief  Read generic device register (platform dependent)
 *
 * @param  handle    customizable argument. In this examples is used in
 *                   order to select the correct sensor bus handler.
 * @param  reg       register to read
 * @param  bufp      pointer to buffer that store the data read
 * @param  len       number of consecutive register to read
 *
 */
static int32_t platform_read(void *handle, uint8_t reg, uint8_t *bufp,
                             uint16_t len)
{
#if defined(NUCLEO_F401RE)
  HAL_I2C_Mem_Read(handle, IIS2DULPX_I2C_ADD_L, reg,
                   I2C_MEMADD_SIZE_8BIT, bufp, len, 1000);
#elif defined(STEVAL_MKI109V3)
  reg |= 0x80;
  HAL_GPIO_WritePin(CS_up_GPIO_Port, CS_up_Pin, GPIO_PIN_RESET);
  HAL_SPI_Transmit(handle, &reg, 1, 1000);
  HAL_SPI_Receive(handle, bufp, len, 1000);
  HAL_GPIO_WritePin(CS_up_GPIO_Port, CS_up_Pin, GPIO_PIN_SET);
#elif defined(SPC584B_DIS)
  i2c_lld_read(handle, IIS2DULPX_I2C_ADD_L & 0xFE, reg, bufp, len);
#endif
  return 0;
}

/*
 * @brief  platform specific outputs on terminal (platform dependent)
 *
 * @param  tx_buffer     buffer to transmit
 * @param  len           number of byte to send
 *
 */
static void tx_com(uint8_t *tx_buffer, uint16_t len)
{
#if defined(NUCLEO_F401RE)
  HAL_UART_Transmit(&huart2, tx_buffer, len, 1000);
#elif defined(STEVAL_MKI109V3)
  CDC_Transmit_FS(tx_buffer, len);
#elif defined(SPC584B_DIS)
  sd_lld_write(&SD2, tx_buffer, len);
#endif
}

/*
 * @brief  platform specific delay (platform dependent)
 *
 * @param  ms        delay in ms
 *
 */
static void platform_delay(uint32_t ms)
{
#if defined(NUCLEO_F401RE) | defined(STEVAL_MKI109V3)
  HAL_Delay(ms);
#elif defined(SPC584B_DIS)
  osalThreadDelayMilliseconds(ms);
#endif
}

/*
 * @brief  platform specific initialization (platform dependent)
 */
static void platform_init(void)
{
#if defined(STEVAL_MKI109V3)
  TIM3->CCR1 = PWM_3V3;
  TIM3->CCR2 = PWM_3V3;
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);
  HAL_Delay(1000);
#endif
}
